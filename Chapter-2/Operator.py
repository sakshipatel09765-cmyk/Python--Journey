# Arithmatic operator

x=8
y=2

print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x%y)
print(x**y)
print("\n")

#Comparison operator

print(x==y)
print(x<y)
print(x>y)
print(x<=y)
print(x>=y)
print(x!=y)
print("\n")

# Logical operator

print(x>y and x<y)  # Need both condition to be true for -> true
print(x>y or x<y)   # If any one condition is true then -> true
print(not(x>y and x<y))
print("\n")

# Assignment operator
x+=y   # x = x + y
print(x)
x-=y   # x = x - y
print(x) 